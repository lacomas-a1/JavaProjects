package Loop;

public class WhileLoopDiff {
    public static void main(String[]args){
        int Number=0;

        while (Number>10){
            System.out.println("Number is Greater Than 10");
        }
        System.out.println("This Statement is COmming Outside while loop");
    }
}

//n this Java program, we are declaring integer variable Number and assigned value zero to it. Next, We will check whether Number (value = 0) is greater than ten or not to fail the condition deliberately. There is one more System.out.println statement outside the While loop and this statement will execute after the while loop.
