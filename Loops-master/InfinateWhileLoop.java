package Loops;

public class InfinateWhileLoop {
    public static void main(String[]args){
        int Number = 1;

        while(Number <= 10){
            System.out.format("Number=%d",Number);
        }
    }
}

// in this Java infinite While loop example, the number is forever 1, and it is ever less than ten. So, it will go on executing the statement infinite times.
