public class Simple1 {
    public void add(int a,int b){ //F1 function
        System.out.format("The Sum of Two Values=%d\n",(a+b));
    }

    public void add(int a,float b){ //F2 function
        System.out.format("The Sum of Int & Float Values=%.4f\n",(a+b));
    }

    public void add(float a,int b){ //F3 function
        System.out.format("The Sum of Int & Float Values=%.4f\n",(a+b));
    }


}
